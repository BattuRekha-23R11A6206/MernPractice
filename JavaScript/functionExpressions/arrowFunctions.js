// arrow functions are a more concise syntax for writing function expressions
// they are always anonymous

// syntax : (parameters) => { function body }

// arrow function with parameters
const add = (a, b) => {
  return a + b;
};

//they are good for simple functions that you use only once or twice
// if the function has only one expression, you can 
// omit the curly braces and the return keyword
const multiply = (a, b) => a * b;


